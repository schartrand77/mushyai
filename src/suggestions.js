function tokenize(text) {
  return text
    .toLowerCase()
    .split(/[\s,.;:!?()"]+/)
    .filter(Boolean);
}

export function getFavoriteKeywords(jobs, max = 10) {
  const favoritedJobs = jobs.filter((job) => job.isFavorite);
  if (favoritedJobs.length === 0) {
    return [];
  }

  const wordCounts = favoritedJobs.reduce((counts, job) => {
    const tokens = tokenize(job.prompt);
    tokens.forEach((token) => {
      if (!STOP_WORDS.includes(token) && token.length > 2) {
        counts[token] = (counts[token] || 0) + 1;
      }
    });
    return counts;
  }, {});

  return Object.entries(wordCounts)
    .sort(([, a], [, b]) => b - a)
    .slice(0, max)
    .map(([word]) => word);
}
